// Helper function to get icon for any option
export const getIcon = (type: 'cut' | 'seasoning' | 'sauce', name: string): React.ReactNode => {
  if (type === 'cut') {
    return CutIcons[name] || CutIcons['Straight Cut'];
  }
  if (type === 'seasoning') {
    return SeasoningIcons[name] || SeasoningIcons['Classic Salt'];
  }
  if (type === 'sauce') {
    return SauceIcons[name] || SauceIcons['Ketchup'];
  }
  return null;
};
